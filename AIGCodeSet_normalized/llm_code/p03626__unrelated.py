v1 = 10 ** 9 + 7

def f1(a1, a2, a3):
    v1 = [[0] * 3 for v2 in range(a1)]
    v1[0][0] = v1[0][1] = v1[0][2] = 1
    for v3 in range(1, a1):
        if a2[v3] == a2[v3 - 1]:
            v1[v3][0] = (v1[v3 - 1][1] + v1[v3 - 1][2]) % v1
            v1[v3][1] = (v1[v3 - 1][0] + v1[v3 - 1][2]) % v1
            v1[v3][2] = (v1[v3 - 1][0] + v1[v3 - 1][1]) % v1
        else:
            v1[v3][0] = (v1[v3 - 1][1] + v1[v3 - 1][2]) % v1
            v1[v3][1] = (v1[v3 - 1][0] + v1[v3 - 1][2]) % v1
            v1[v3][2] = (v1[v3 - 1][0] + v1[v3 - 1][1]) % v1
        if a3[v3] == a3[v3 - 1]:
            v1[v3][0] = v1[v3][0] * (v1[v3 - 1][1] + v1[v3 - 1][2]) % v1
            v1[v3][1] = v1[v3][1] * (v1[v3 - 1][0] + v1[v3 - 1][2]) % v1
            v1[v3][2] = v1[v3][2] * (v1[v3 - 1][0] + v1[v3 - 1][1]) % v1
        else:
            v1[v3][0] = v1[v3][0] * (v1[v3 - 1][1] + v1[v3 - 1][2]) % v1
            v1[v3][1] = v1[v3][1] * (v1[v3 - 1][0] + v1[v3 - 1][2]) % v1
            v1[v3][2] = v1[v3][2] * (v1[v3 - 1][0] + v1[v3 - 1][1]) % v1
    return sum(v1[-1]) % v1
v2 = int(input())
v3 = input()
v4 = input()
print(f1(v2, v3, v4))
